def get_loan_purpose_dropdown_by_product_line(product_line_code):
    product = ProductLine.objects.get_or_none(pk=product_line_code)

    if product:
        loan_purposes = product.loan_purposes.all().order_by('id')
        if not loan_purposes:
            return None
        purposes = list([x.purpose for x in loan_purposes])
        versions = list([x.version for x in loan_purposes])
        version = versions[-1:]
        data = {
            'product_line_code': product_line_code,
            'version': version,
            'results': purposes,
        }
        return data
    return None
